// === JS Cheatsheet: Strings (basic) ==========

// --- General
const text = 'Hello, World!'; // Normal String
const text = 'I\'m Happy!';	// Escape Character

// --- Multiline wrapping in editor
const text = 'This is \
a very long \
string that is wrapped';
// Strings can be wrapped with a backslash.
// There can't be any space after the Backslash.
// The output won't be affected

// --- Template Literals
// Concatenate strings
const world = 'world';
const text = `Hello ${world}`;

// Literal wrapping
const text = `This string
will be
	wrapped and 
outputted how it literally stands here`;